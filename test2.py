# Variable adalah tempat menyimpan data

# Menaruh / assignment nilai
a = 10
x = 5
panjang = 1000

# Pemanggilan pertama
print("Nilai a =", a)
print("Nilai x =", x)
print("Nilai panjang =", panjang)

# Penamaan
# underscore boleh tapi spasi gaboleh | gaboleh naro angka di depan juga (10juta)
nilai_y = 15
juta10 = 10000000
nilaiZ = 17.5

# Pemanggilan kedua
print("Nilai nilai_y =", nilai_y)
nilai_y = 21
print("Nilai nilai_y =", nilai_y)
print("Nilai juta10 =", juta10)
print("Nilai nilaiZ =", nilaiZ)

# Assignment indirect
b = nilai_y
print("Nilai b =", b)

